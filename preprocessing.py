import pandas as pd


# 데이터 로드
df = pd.read_csv('airkorea_2024_seongbuk.csv', encoding='utf-8')
print(f'[원본] {df.shape[0]}행 {df.shape[1]}열')


# =====================================================================
# 측정일시 -> 월, 시간대
# =====================================================================
s = df['측정일시'].astype(str)
df['월']    = s.str[4:6].astype(int)         # 5~6번째 문자 = 월
df['시간대'] = s.str[8:10].astype(int) % 24   # 9~10번째 문자 = 시 (24시 -> 0시)


# =====================================================================
# 불필요한 컬럼 제거
# ---------------------------------------------------------------------
# - 지역/망: 필요 x
# - 측정소코드/측정소명/주소: 식별자 -> 예측과 무관
# - 측정일시: 월/시간대로 이미 변환 완료
# - 미세먼지(PM10): 타겟(PM2.5)과 연관이 강하므로 피처에서 제외
# =====================================================================
drop_cols = ['지역', '망', '측정소코드', '측정소명', '측정일시', '주소', '미세먼지(PM10)']
df = df.drop(columns=drop_cols)


# =====================================================================
# 결측치 제거 - 데이터가 하나라도 비어있는 경우 제거
# =====================================================================
before = len(df)
df = df.dropna().reset_index(drop=True)
print(f'[결측치 제거] {before}행 -> {len(df)}행 ({before-len(df)}행 제거)')


# =====================================================================
# 정제 결과 저장
# =====================================================================
print(f'[최종 컬럼] {df.columns.tolist()}')
df.to_csv('airkorea_2024_seongbuk_cleaned.csv', index=False, encoding='utf-8-sig')
print(f'[저장 완료] airkorea_2024_seongbuk_cleaned.csv ({len(df)}행)')
